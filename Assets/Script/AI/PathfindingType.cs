///this enum defines different types of pathfinding algorithms that can be used in AI navigation
///Death-First Search (DFS), Breadth-First Search (BFS), and A* (AStar)
public enum PathfindingType
{
    DFS,
    BFS,
    AStar
}

/*
HOW THIS SCRIPT WORKS:
This enum defines which pathfinding algorithm is currently selected
It allows the project to switch between different pathfinding behaviors
without changing the core logic of the Pathfinding system
*/
